  [InlineData(10, 7, 9, 6, 9)]
  [InlineData(10, 6, 8, 4, 5)]
  [InlineData(9, 6, 8, 4, 4)]
  public void Solve_MazeWithLoopsExample_ReturnsCorrectShortestRoute(int startRow, int startCol,
                                                                     int finishRow, int finishCol,
                                                                     int expectedLength) {
    // Arrange
    var maze = MazeWithLoopsExample;
    var start = new Cell(startRow - 1, startCol - 1);
    var finish = new Cell(finishRow - 1, finishCol - 1);
    var solver = new QLearningSolver(maze, finish, randomSeed: 21, saveLogs: false);
    solver.Fit();
    // Act
    var route = solver.Solve(start);
    var routeLength = route.Count;

    // Assert
    Assert.True(HelpersForTests.IsCorrectRoute(maze, route.ToArray(), start, finish));
    Assert.Equal(expectedLength, routeLength);
  }

